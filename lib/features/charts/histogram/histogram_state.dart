import '../chart_state.dart';

/// {@template histogram_state}
/// Состояние гистограммы для системы плагинов
/// 
/// Хранит настройки отображения гистограммы:
/// - [bins] — количество корзин (интервалов) для группировки данных
/// - [columnName] — имя выбранной числовой колонки для анализа
/// 
/// Наследуется от [ChartState] для интеграции с системой плагинов.
/// {@endtemplate}
class HistogramState extends ChartState {
  /// Количество корзин (интервалов) гистограммы
  /// 
  /// Определяет степень детализации распределения.
  /// Обычно значение находится в диапазоне от 5 до 50.
  int bins;

  /// Имя выбранной числовой колонки для отображения
  String? columnName;

  /// {@macro histogram_state}
  HistogramState({
    this.bins = 10,
    this.columnName,
  });
}