/// {@template hover_range}
/// Диапазон значений для подсветки ячеек тепловой карты при наведении на легенду.
/// 
/// Используется для передачи информации о том, какие ячейки должны быть подсвечены:
/// - Для дискретного режима: [min] и [max] определяют интервал значений.
/// - Для градиентного режима: [value] — центральное значение.
/// 
/// При наведении на легенду создается объект этого класса, который передается
/// в [HeatmapPainter] для подсветки соответствующих ячеек.
/// {@endtemplate}
class HoverRange {
  /// Нижняя граница диапазона (для дискретного режима).
  final double? min;

  /// Верхняя граница диапазона (для дискретного режима).
  final double? max;

  /// Конкретное значение (для градиентного режима).
  final double? value;

  /// {@macro hover_range}
  HoverRange({this.min, this.max, this.value});
}